def vydelit_zadachi(analizirovannyi_transkript: str) -> str:
    prompt = f"""
    Твоя задача: проанализировать разговор и выделить все задачи, поручения и обязательства.
    1. Определение задач
    - Найди все формулировки, где:
    - кому-то что-то поручают
    - есть просьбы выполнить действие
    - есть распределение обязанностей
    - есть договорённости о выполнении работы
    
    2. Если задачи есть
    Для каждой задачи укажи:
    
    - номер задачи
    - что нужно сделать (кратко и ясно)
    - исполнитель (имя или роль участника)
    - срок выполнения (если указан)
    
    Если срок не указан — пиши: "Не указано"
    
    3. Если задач нет
    - Выведи строго: "Нет задач"
    
    4. Формат вывода (если задачи есть)
    - только нумерованный список
    - без лишнего текста
    - без пояснений
    
    Пример:
    
    1. Сделать отчёт — Иван (менеджер) — Не указано  
    2. Проверить код — Анна (разработчик) — 25.04.2026
    
    5. Правила обработки
    - Не добавляй новые задачи, которых нет в тексте
    - Не додумывай смысл
    - Не интерпретируй разговор вне явного поручения
    - Если задача неясна — всё равно включи, но пометь исполнителя как "Неизвестно"

    Транскрипт:
    {analizirovannyi_transkript}
    """
    return poluchit_otvet_ot_llm(prompt)